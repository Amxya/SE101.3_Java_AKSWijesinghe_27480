package com.mycompany.q3;
public class Q3 {

    public static void main(String[] args) {
       
        System.out.println("For loop");
        for(int a = 0; a < 5; a++){
            System.out.println("Executing loop "+a);
            a++;
        }
        
        System.out.println("");
        System.out.println("While loop");
        int i = 0;
        while (i < 5){
           System.out.println("Executing loop "+i);
           i++;
       }
        
    }
}
